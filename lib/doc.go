// Implements libraries to accept an input directory's contents, retrieve time
// created and sort organized directories.

// as a tool for folks who have large directories with hundreds of thousands of
// media (jpgs, raw photos, tiff, avi, etc) and want to have them sorted by
// Year, Month or Day in nested directories. 

// The CLI allows the user to:
// eval one file - exifSort eval <file>
// scan a directory recursively - exifSort scan <dir>
// sort a directory into a newly created output directory in time structure - exifSort sort <
// retrieve the time info for each file, or to sorth them in nested
// directories. 
package exifSort
